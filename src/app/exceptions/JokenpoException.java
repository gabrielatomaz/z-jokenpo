package app.exceptions;

public class JokenpoException extends Exception {

    private static final long serialVersionUID = 1L;

    public JokenpoException() {
		super();
	}

	public JokenpoException(String message, Throwable cause) {
		super(message, cause);
	}

	public JokenpoException(String message) {
		super(message);
	}

	public JokenpoException(Throwable cause) {
		super(cause);
	}
}